function draw()
  for i,v in ipairs(LevelMap) do
    for j,w in ipairs(LevelMap[i]) do
      if w == 'X' then
        love.graphics.setColor(0, 0, 255)
        love.graphics.rectangle("fill", (j-1)*32, (i-1)*32, 32, 32)
